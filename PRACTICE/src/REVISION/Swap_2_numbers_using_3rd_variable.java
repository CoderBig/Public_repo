package REVISION;

public class Swap_2_numbers_using_3rd_variable {

	public static void main(String[] args) {
		int A=3; int B= 4; int temp;
		temp=A;// Assign value of A to temp
		A=B;//Assign value of B to A
		B=temp;//Assign value of temp i.e. A to B
		System.out.println("The Swapped nos are A="+" "+A+ " And B=  "+B);
	}

}
